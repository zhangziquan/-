package com.example.zhangziquan.personalproject5;

public class Repo {
    int id;
    String name;
    String description;
    Boolean has_issues;
    int open_issues;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public Boolean getHas_issues() {
        return has_issues;
    }

    public int getOpen_issues() {
        return open_issues;
    }
}
